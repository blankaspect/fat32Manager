/*====================================================================*\

StrConv.h

Class: string-conversion methods.

\*====================================================================*/

#ifndef _DRIVEIO_STR_CONV_H_
#define _DRIVEIO_STR_CONV_H_

//----------------------------------------------------------------------


// INCLUDES


#ifndef _DRIVEIO_TYPES_H_
	#include "types.h"
#endif

//----------------------------------------------------------------------


// CLASS: STRING-CONVERSION METHODS


class StrConv
{

////////////////////////////////////////////////////////////////////////
//  Constructors
////////////////////////////////////////////////////////////////////////

private:

	StrConv()
	{
	}

	//------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Class methods
////////////////////////////////////////////////////////////////////////

public:

	static std::u16string utf8ToUtf16(
		const std::string&	str);

	//------------------------------------------------------------------

	static std::u32string utf8ToUtf32(
		const std::string&	str);

	//------------------------------------------------------------------

	static std::string utf16ToUtf8(
		const std::u16string&	str);

	//------------------------------------------------------------------

	static std::u32string utf16ToUtf32(
		const std::u16string&	str);

	//------------------------------------------------------------------

	static std::string utf32ToUtf8(
		const std::u32string&	str);

	//------------------------------------------------------------------

	static std::u16string utf32ToUtf16(
		const std::u32string&	str);

	//------------------------------------------------------------------

	static std::wstring utf32ToWstr(
		const std::u32string&	str);

	//------------------------------------------------------------------

	static std::u32string wstrToUtf32(
		const std::wstring&	str);

	//------------------------------------------------------------------

	static std::wstring strToWstr(
		const std::string&	str);

	//------------------------------------------------------------------

	static std::string wstrToStr(
		const std::wstring&	str);

	//------------------------------------------------------------------

};

//----------------------------------------------------------------------

#endif	// _DRIVEIO_STR_CONV_H_

//----------------------------------------------------------------------
