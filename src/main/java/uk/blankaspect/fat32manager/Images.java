	private static Image applyHueMap(
		Image	image)
	{
		// If style manager has not been initialised, return input image
		StyleManager styleManager = StyleManager.INSTANCE;
		if (styleManager == null)
			return image;

		// Get hue map for theme
		List<HueMapEntry> hueMap = switch (styleManager.getThemeId())
		{
			case uk.blankaspect.ui.jfx.style.themes.dark.Theme.ID  -> HUE_MAP_DARK;
			case uk.blankaspect.ui.jfx.style.themes.light.Theme.ID -> HUE_MAP_LIGHT;
			default                                                -> throw new UnexpectedRuntimeException();
		};

		// Get dimensions of input image
		int width = (int)image.getWidth();
		int height = (int)image.getHeight();

		// Initialise output image
		WritableImage outImage = new WritableImage(width, height);

		// Apply hue map to pixels of image
		if ((width > 0) && (height > 0))
		{
			PixelReader reader = image.getPixelReader();
			PixelWriter writer = outImage.getPixelWriter();
			for (int y = 0; y < height; y++)
			{
				for (int x = 0; x < width; x++)
				{
					Color colour = reader.getColor(x, y);
					double hue = colour.getHue();
					for (HueMapEntry entry : hueMap)
					{
						if (entry.hue == hue)
						{
							colour = Color.hsb(0.0, 0.0, entry.brightness, colour.getOpacity());
							break;
						}
					}
					writer.setColor(x, y, colour);
				}
			}
		}

		// Return output image
		return outImage;
	}

